package Interview_Projects;

public class SumOfArrays {
	
	
	    public static void main(String[] args) {
	        int[] array = {20, 30, 40, 50, 60};
	        
	       int sum = 0;
	    	   for (int i=0; i<array.length; i++) {
	    		   sum += array[i];

	    		   System.out.println("sum of array is " + sum);
	    	   }
	       }
	}



