package Java_shammiclasses;

public class assignment_operators {

	public static void main(String[] args) {
		
		int a= 5;
		
		a += 5;
		System.out.println(a); // 10
		
		a -= 2;
		System.out.println(a); // 8
		
		a *= 2;
		System.out.println(a); // 16
		
		a /= 4;
		System.out.println(a); // 4
		
		

	}

}
