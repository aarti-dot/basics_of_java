package Java_shammiclasses;

public class WhileLoop1 {

	public static void main(String[] args) {
		int i= 1;
		while (i<10) {
			System.out.println(i);
			i++;
		}
		int j=2;
		while (j<10) {
			System.out.println(j);
			j+=2;
		}
		int p=1;
		while(p<=10) {
			if (p%2==0) {
				System.out.println("even");
			}else {
				System.out.println("odd");
			}p++;
		}


	}

}
