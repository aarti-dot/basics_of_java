public class IfElseLadder {

	public static void main(String[] args) {
		int a = 100;
		int b= 200;
		// if else is ladder
		if(a>b) {
			System.out.println("a is greater than b");
		}else if(a==b) {
			System.out.println("a is equal to b");
		}else {
			System.out.println("a is less than b ");
		}

	}

}
