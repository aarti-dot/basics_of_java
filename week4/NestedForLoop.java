package week4;

public class NestedForLoop {	
	
	public static void main(String[] args) 
	{
		
	for (int num=1;num<=10;num++)
	{
		for (int i=1;i<=10;i++)
		{
			System.out.print(num*i+"\t");
		}
		System.out.println();
		
	}
	System.out.println("*************************");
	
	for (int i=1;i<=10;i++)
	{
		if (i>=5)
		{
			System.out.print(i);
		}else

			System.out.println(i);
			
		}
		for (int r=1;r<=5;r++)
		{
			for(int c=1;c<=5;c++)
			{
				System.out.print("*");
				
			}
			System.out.println();
		}
	
	{
		System.out.println("********************");
		
	for (int r=1;r<=4;r++)

		{
			for(int c=1;c<=r;c++)
			{
				System.out.print("*"+" ");
			}
			System.out.println();
			

		}
	System.out.println("*******************");
	
	for (int n=1;n<=4;n++)
	{
		for(int m=4;m>=1;m++)
		{
			System.out.print("*"+"");
		}
		System.out.println();
		
	}
	//for (int x=1;x<=10;x++)
	{
	//	if (x=1)
	}

	}
		
	}	
	}
		
	
	
		
	
