package array;

import java.util.Arrays;

public class SortingArray {

	public static void main(String[] args) {
		
		char s[] = {'z', 's', 'a', 'm', 'b'};
		System.out.println("Before sorting arrays " + Arrays.toString(s));
		
		Arrays.sort(s);
		System.out.println("After sorting arrays " + Arrays.toString(s));
		
		
		



	}

}
