package Java_2024;

import java.util.Scanner;

public class Swapping_number {

	public static void main(String[] args) {
		
		
		int a = 20;
		int b = 30;
		
		
		System.out.println("before swapping a and b = " + a +","+  b );
		
		a = a+b; //50
		b = a-b; //20
		a = a-b;
		
		System.out.println("After swapping a and b = " + a + "," + b );
		
		



	}

}
